<p>During the first access NextCloud will ask information to configure itself. This information includes:</p>
<ul>
  <li>An admin account name and password. Please choose a strong password, especially if NextCloud will be exposed to the internet.</li>
  <li>Storage configuration. For small deployments, leave the default values (SQLite).</li>
</ul>
<p>If everything went fine after clicking &quot;Finish setup&quot;, there will be a &quot;Welcome to NextCloud&quot; page.</p>
